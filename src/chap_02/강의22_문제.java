package chap_02;

public class 강의22_문제 {
    public static void main(String[] args) {
        // 키가 120 이상만 탑승 가능
        int i = 115;
        String 결과 = (i >= 120) ? "가능" : "불가능";
        System.out.println("키가 " + i + "cm 이므로 탑승이 " + 결과 + "합니다.");

        int ii = 121;
        String 결과2 = (ii >= 120) ? "가능" : "불가능";
        System.out.println("키가 " + ii + "cm 이므로 탑승이 " + 결과2 + "합니다.");



    }
}
